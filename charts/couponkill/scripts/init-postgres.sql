-- =============================================================================
-- CouponKill PostgreSQL 初始化脚本（MySQL -> PostgreSQL 迁移）
-- 通过 psql 执行： psql -U postgres -h <host> -f init-postgres.sql
--
-- 库表布局与 ShardingSphere 分片规则严格对齐：
--   user  服务 : user_db_0 / user_db_1        -> 表 "user"、user_coupon_count（逻辑表不分表）
--   order 服务 : order_db_0 / order_db_1       -> 表 order_0 .. order_15
--   coupon服务 : coupon_db_0 / coupon_db_1     -> 表 coupon_0 .. coupon_15、stock_log_0 .. stock_log_15
--
-- 字段以 common 模块 POJO + MyBatis Mapper 为唯一真源（不再沿用历史 init.sql 中漂移的字段名）。
-- =============================================================================

-- ---------------------------------------------------------------------------
-- 1. 创建 6 个分片数据库（幂等）
-- ---------------------------------------------------------------------------
SELECT 'CREATE DATABASE user_db_0'   WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname = 'user_db_0')\gexec
SELECT 'CREATE DATABASE user_db_1'   WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname = 'user_db_1')\gexec
SELECT 'CREATE DATABASE order_db_0'  WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname = 'order_db_0')\gexec
SELECT 'CREATE DATABASE order_db_1'  WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname = 'order_db_1')\gexec
SELECT 'CREATE DATABASE coupon_db_0' WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname = 'coupon_db_0')\gexec
SELECT 'CREATE DATABASE coupon_db_1' WHERE NOT EXISTS (SELECT FROM pg_database WHERE datname = 'coupon_db_1')\gexec

-- ---------------------------------------------------------------------------
-- 2. user 服务库表（user_db_0 / user_db_1）
--    "user" 为 PG 保留字，需双引号；create/update_time 由应用显式写入，
--    故无需 MySQL 的 ON UPDATE CURRENT_TIMESTAMP。
-- ---------------------------------------------------------------------------
\c user_db_0

CREATE TABLE IF NOT EXISTS "user" (
    id               BIGINT       GENERATED BY DEFAULT AS IDENTITY,
    username         VARCHAR(50)  NOT NULL UNIQUE,
    password         VARCHAR(100) NOT NULL,
    phone            VARCHAR(20),
    email            VARCHAR(100),
    status           INT          NOT NULL DEFAULT 1,
    create_time      TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    update_time      TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    last_active_time TIMESTAMP,
    PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS user_coupon_count (
    user_id       BIGINT    NOT NULL,
    total_count   INT       NOT NULL DEFAULT 0,
    seckill_count INT       NOT NULL DEFAULT 0,
    normal_count  INT       NOT NULL DEFAULT 0,
    expired_count INT       NOT NULL DEFAULT 0,
    update_time   TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    version       INT       NOT NULL DEFAULT 0,
    PRIMARY KEY (user_id)
);

