-- ---------------------------------------------------------------------------
-- 3. order 服务库表（order_db_0 / order_db_1，每库 order_0 .. order_15）
--    表名带下划线后缀后不再是保留字；id 为业务生成的字符串（雪花/UUID）。
-- ---------------------------------------------------------------------------
\c order_db_0
DO $$
DECLARE i INT;
BEGIN
    FOR i IN 0..15 LOOP
        EXECUTE format($f$
            CREATE TABLE IF NOT EXISTS order_%s (
                id              VARCHAR(64) NOT NULL,
                user_id         BIGINT      NOT NULL,
                coupon_id       BIGINT      NOT NULL,
                virtual_id      VARCHAR(64) NOT NULL,
                status          INT         NOT NULL DEFAULT 1,
                get_time        TIMESTAMP   NOT NULL,
                expire_time     TIMESTAMP   NOT NULL,
                use_time        TIMESTAMP,
                cancel_time     TIMESTAMP,
                create_time     TIMESTAMP   NOT NULL,
                update_time     TIMESTAMP   NOT NULL,
                created_by_java INT         NOT NULL DEFAULT 0,
                created_by_go   INT         NOT NULL DEFAULT 0,
                request_id      VARCHAR(64) NOT NULL,
                version         INT         NOT NULL DEFAULT 0,
                PRIMARY KEY (id),
                CONSTRAINT uk_order_%s_user_coupon UNIQUE (user_id, coupon_id)
            );
            CREATE INDEX IF NOT EXISTS idx_order_%s_user_id     ON order_%s (user_id);
            CREATE INDEX IF NOT EXISTS idx_order_%s_coupon_id   ON order_%s (coupon_id);
            CREATE INDEX IF NOT EXISTS idx_order_%s_virtual_id  ON order_%s (virtual_id);
            CREATE INDEX IF NOT EXISTS idx_order_%s_create_time ON order_%s (create_time);
        $f$, i, i, i, i, i, i, i, i, i, i);
    END LOOP;

    -- P0 预约帮抢表（单库，配合 ShardingSphere SINGLE → order-db-0）
    CREATE TABLE IF NOT EXISTS seckill_reservation (
        id            BIGSERIAL PRIMARY KEY,
        user_id       BIGINT       NOT NULL,
        coupon_id     BIGINT       NOT NULL,
        status        VARCHAR(32)  NOT NULL DEFAULT 'PENDING',
        reserve_at    TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
        trigger_at    TIMESTAMPTZ,
        fired_at      TIMESTAMPTZ,
        request_id    VARCHAR(64),
        order_id      VARCHAR(64),
        fail_code     INT,
        fail_reason   VARCHAR(256),
        retry_count   INT          NOT NULL DEFAULT 0,
        version       INT          NOT NULL DEFAULT 0,
        create_time   TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
        update_time   TIMESTAMPTZ  NOT NULL DEFAULT NOW()
    );
    CREATE UNIQUE INDEX IF NOT EXISTS uk_reservation_active_user_coupon
        ON seckill_reservation (user_id, coupon_id)
        WHERE status IN ('PENDING', 'FIRING', 'QUEUED');
    CREATE INDEX IF NOT EXISTS idx_reservation_pending_trigger
        ON seckill_reservation (status, trigger_at)
        WHERE status = 'PENDING';
    CREATE INDEX IF NOT EXISTS idx_reservation_user
        ON seckill_reservation (user_id, create_time DESC);
    CREATE INDEX IF NOT EXISTS idx_reservation_request
        ON seckill_reservation (request_id)
        WHERE request_id IS NOT NULL;

    -- 站内通知（预约结果等；单库 order-db-0）
    CREATE TABLE IF NOT EXISTS user_notification (
        id            BIGSERIAL PRIMARY KEY,
        user_id       BIGINT       NOT NULL,
        type          VARCHAR(64)  NOT NULL,
        title         VARCHAR(128) NOT NULL,
        content       VARCHAR(512),
        ref_id        BIGINT,
        read_flag     BOOLEAN      NOT NULL DEFAULT FALSE,
        create_time   TIMESTAMPTZ  NOT NULL DEFAULT NOW()
    );
    CREATE INDEX IF NOT EXISTS idx_notification_user
        ON user_notification (user_id, create_time DESC);
    CREATE INDEX IF NOT EXISTS idx_notification_unread
        ON user_notification (user_id)
        WHERE read_flag = FALSE;
END $$;

