-- ---------------------------------------------------------------------------
-- 4. coupon 服务库表（coupon_db_0 / coupon_db_1）
--    coupon_0 .. coupon_15 与 stock_log_0 .. stock_log_15，均按 shard_index 分片。
--    coupon 复合主键 (id, shard_index)，其 GROUP BY id, shard_index 在 PG 下依赖
--    主键函数依赖成立，SELECT * 合法。
-- ---------------------------------------------------------------------------
\c coupon_db_0
DO $$
DECLARE i INT;
BEGIN
    FOR i IN 0..15 LOOP
        EXECUTE format($f$
            CREATE TABLE IF NOT EXISTS coupon_%s (
                id                     BIGINT        NOT NULL,
                shard_index            INT           NOT NULL,
                name                   VARCHAR(100)  NOT NULL,
                description            TEXT,
                type                   INT           NOT NULL,
                face_value             NUMERIC(10,2),
                min_spend              NUMERIC(10,2) DEFAULT 0.00,
                valid_days             INT           NOT NULL DEFAULT 15,
                per_user_limit         INT           NOT NULL DEFAULT 1,
                total_stock            INT           NOT NULL DEFAULT 0,
                seckill_total_stock    INT           NOT NULL DEFAULT 0,
                remaining_stock        INT           NOT NULL DEFAULT 0,
                seckill_remaining_stock INT          NOT NULL DEFAULT 0,
                status                 INT           NOT NULL DEFAULT 1,
                seckill_start_at       TIMESTAMPTZ,
                seckill_end_at         TIMESTAMPTZ,
                create_time            TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
                update_time            TIMESTAMP     DEFAULT CURRENT_TIMESTAMP,
                version                INT           NOT NULL DEFAULT 0,
                PRIMARY KEY (id, shard_index)
            );
        $f$, i);
        EXECUTE format($f$
            CREATE TABLE IF NOT EXISTS stock_log_%s (
                id           BIGINT GENERATED BY DEFAULT AS IDENTITY,
                coupon_id    BIGINT,
                order_id     BIGINT,
                activity_id  BIGINT,
                quantity     INT,
                operate_type INT,
                operate_id   BIGINT,
                stock_after  INT,
                remark       VARCHAR(255),
                create_time  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                shard_index  INT NOT NULL DEFAULT 0,
                PRIMARY KEY (id, shard_index)
            );
        $f$, i);
    END LOOP;
END $$;

